export const defaultDatetimeFormat = dateTime => {
  if (!dateTime) return '';
  const dateString = `${dateTime.getDate()}.${dateTime.getMonth() + 1}.`;
  const timeString = `${dateTime.getHours()}:${dateTime.getMinutes()}`;
  return `${dateString} ${timeString}`;
};

